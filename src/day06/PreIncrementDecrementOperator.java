public class PreIncrementDecrementOperator {
    public static void main(String[] args) {


        int offerCount = 3;

//        offerCount += 1;

        // ++ doing the same job as the one above
        ++  offerCount;  //increase it by 1 its unique for __1__
        ++  offerCount;

        // ++ or -- //

        System.out.println(offerCount);

        -- offerCount;

        System.out.println(offerCount);




    }
}
