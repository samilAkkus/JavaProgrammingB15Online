package day06;

public class CompoundOperator2 {

    public static void main(String[] args) {



        // += , -=, *= , /=, %= are called shorthand or compound operator.

        int studentOnline = 263;

        studentOnline += 5;

        System.out.println(studentOnline);

        studentOnline -= 3;

        System.out.println(studentOnline);






    }
}
