package day18;

public class ForLoopIntro {

    public static void main(String[] args) {


        //count 1 to 5

        for( int x = 1 ; x<=5 ; ++x ){

            System.out.println("happy birthday");
        }

        for( int counter = 10 ; counter > 0 ; --counter ){

            System.out.println(counter+" hayirli cumalar");
        }


    }
}
