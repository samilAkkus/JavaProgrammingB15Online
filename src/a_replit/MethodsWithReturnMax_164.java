package a_replit;

public class MethodsWithReturnMax_164 {

    public static void main(String[] args) {

        max(2,5);

        System.out.println(max(2,5));

    }

    public static int max(int x, int max)
    {

        if(x > max){

            return max;
        }else {

            return x;
        }

    }
}
