package a_replit;

public class PrintLettersCombination_091 {

    public static void main(String[] args) {

        String bla = "zyxwv";

        for (int i = 0; i <=bla.length()-1 ; i++) {


            for (int j = 0; j <=bla.length()-1 ; j++) {

                System.out.println(""+bla.charAt(i)+bla.charAt(j));

            }



        }



    }
}
